#include <unistd.h>

void	ft_print_comb(void)
{
	int	a;
	int	b;
	int	c;

	a = '0';
	while (a <= '9')
	{
		b = a +1;
		while (b <= '9')
		{
			c = b +1;
			while (c <= '9')
			{
				if ((a != '0') || (b != '1') || (c != '2'))
					write(1, &", ", 2);
				write (1, &a, 1);
				write (1, &b, 1);
				write (1, &c, 1);
				c = c +1;
			}
			b = b + 1;
		}
		a = a + 1;
	}
}
